def run_sensitivity_scan(mode='all', baseline_result=None):
    """Run the sweep(s) selected by `mode`.

    mode in {'all', 'fuel', 'h2', 'power', 'fuel_rho', 'beo', 'h2_T',
             'fuel_radius'}.
    Reactivity is always computed relative to a baseline case.

    Parameters
    ----------
    baseline_result : dict, optional
        A pre-computed baseline result dict (as produced by run_case()).
        When supplied the baseline OpenMC run is skipped entirely and this
        dict is used instead.  Pass this when the baseline has already been
        computed in a previous invocation to avoid re-running it for every
        partial sweep.  If None (default) the baseline is run as normal and
        a standalone copy is saved to <RESULTS_DIR>/baseline.json so it can
        be reused in future calls via load_baseline().
    """
    if mode not in ('all', 'fuel', 'h2', 'power', 'fuel_rho', 'beo', 'h2_T', 'fuel_radius'):
        raise ValueError(
            f"mode must be one of 'all', 'fuel', 'h2', 'power', 'fuel_rho', "
            f"'beo', 'h2_T', 'fuel_radius'; got {mode!r}"
        )

    os.makedirs(RESULTS_DIR, exist_ok=True)
    os.makedirs(RUNS_DIR,    exist_ok=True)

    base_config = GCRConfig(**BASE_CONFIG_KWARGS)

    results = []

    # Baseline — run only when not provided by the caller.
    if baseline_result is not None:
        print('\n=== Baseline: using pre-computed result (skipping OpenMC run) ===')
        print(f"    k_eff = {baseline_result['keff']:.5f} +/- {baseline_result['sigma']:.5f}")
        results.append(baseline_result)
    else:
        bl = run_case('baseline', 0.0, base_config)
        results.append(bl)
        # Persist a standalone copy so future partial sweeps can skip this run.
        bl_path = os.path.join(RESULTS_DIR, 'baseline.json')
        with open(bl_path, 'w') as _f:
            json.dump(bl, _f, indent=2)
        print(f'Baseline result saved to {bl_path}')

    # Fuel-temperature sweep (kelvin)
    if mode in ('all', 'fuel'):
        for dT in FUEL_T_DELTAS_K:
            results.append(run_case('fuel_T', dT, base_config))

    # H2 density sweep (fractional)
    if mode in ('all', 'h2'):
        for d_rho in H2_RHO_DELTAS:
            results.append(run_case('h2_rho', d_rho, base_config))


    # Fuel density sweep
    if mode in ('all', 'fuel_rho'):
        for d_rho_f in FUEL_RHO_DELTAS:
            results.append(run_case('fuel_rho', d_rho_f, base_config))

    # BeO reflector temperature sweep (kelvin)
    if mode in ('all', 'beo'):
        for dT in BEO_T_DELTAS_K:
            results.append(run_case('beo_T', dT, base_config))

    # H2 propellant temperature sweep (kelvin, additive).
    if mode in ('all', 'h2_T'):
        for dT in H2_T_DELTAS_K:
            results.append(run_case('h2_T', dT, base_config))

    # Fuel-cloud radius sweep (fractional change of R0/R1/R2, constant N_U
    if mode in ('all', 'fuel_radius'):
        for d_r in FUEL_RADIUS_DELTAS:
            results.append(run_case('fuel_radius', d_r, base_config))

    # Tag the output file by mode so partial runs don't clobber a full one.
    suffix = '' if mode == 'all' else f'_{mode}'
    out = os.path.join(RESULTS_DIR, f'keff_results{suffix}.json')
    with open(out, 'w') as f:
        json.dump(results, f, indent=2)
    print(f'\nRaw k_eff results written to {out}')

    return results


def _weighted_linear_fit(x, y, sy):
    """Weighted least-squares fit y = slope*x + intercept, weights = 1/sy^2.

    Returns (slope, intercept, slope_err, intercept_err).
    """
    x  = np.asarray(x,  float)
    y  = np.asarray(y,  float)
    sy = np.asarray(sy, float)
    w  = 1.0 / sy**2
    V  = np.vander(x, 2) # Vandermonde matrix such that V * a = y
    # w V a = w y
    # V_t w V a = V_t w y
    # a = (V_t w V)^-1 V_t w y
    # a is the vector with polynomial factors
    # https://en.wikipedia.org/wiki/Vandermonde_matrix
    cov = np.linalg.inv(V.T @ np.diag(w) @ V)  # weight the entries
    a = cov @ V.T @ (w * y)
    slope, intercept = float(a[0]), float(a[1])
    slope_err     = float(np.sqrt(cov[0, 0]))
    intercept_err = float(np.sqrt(cov[1, 1]))
    return slope, intercept, slope_err, intercept_err

def log_sensitivity(results, variable, ref_value=None):
    """UARL-style sensitivity coefficients SC = (δk/k)/(Δx/x₀).

    Check at J-910900-5, TABLE VI
    """
    baseline = next(r for r in results if r['variable'] == 'baseline')
    k_ref = baseline['keff']
    sub = [r for r in results if r['variable'] == variable]
    if len(sub) < 2:
        return None
    x  = [ (r['delta'] / ref_value if ref_value else r['delta']) for r in sub ]
    y  = [ (r['keff'] - k_ref) / k_ref for r in sub ]
    sy = [  r['sigma'] / k_ref          for r in sub ]
    S, _, S_err, _ = _weighted_linear_fit(x, y, sy)
    return S, S_err

def _rho_slope(results, variable):
    """dρ/d(delta) in pcm from stored k_eff — matches the existing fits.
    delta is a fraction for densities/radius (→ pcm per unit fraction)
    and kelvin for temperatures (→ pcm/K)."""
    sub = [r for r in results if r['variable'] == variable]
    if len(sub) < 2:
        return None
    x  = [r['delta']                          for r in sub]
    y  = [1e5 * (r['keff'] - 1.0) / r['keff'] for r in sub]
    sy = [1e5 * r['sigma'] / r['keff']**2     for r in sub]
    s, _, se, _ = _weighted_linear_fit(x, y, sy)
    return s, se


def practical_coefficient(results, variable):
    """Sensitivity coefficient in report units: densities → pcm/%, temperatures → pcm/K,
    fuel-cloud radius → pcm/mm.  Returns (value, err, unit) or None."""
    result = _rho_slope(results, variable)
    if result is None:
        return None
    slope, slope_error = result

    if variable in ('fuel_T', 'beo_T', 'h2_T'):
        return slope, slope_error, 'pcm/K'                    # delta already in K

    if variable in ('h2_rho', 'fuel_rho', 'power'):
        return slope / 100.0, slope_error / 100.0, 'pcm/%'    # divide by 100 to get %

    if variable == 'fuel_radius':
        R0_mm = GCRConfig(**BASE_CONFIG_KWARGS).R2 * 10.0   # length are defined in cm in openmc
        return slope / R0_mm, slope_error / R0_mm, 'pcm/mm'

    raise ValueError(f'unknown variable {variable!r}')

