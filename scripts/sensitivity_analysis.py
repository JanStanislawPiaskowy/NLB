# Baseline ABSOLUTE temperatures [K] for the UARL dimensionless S of the
# thermal vars. Set these to your unperturbed run's values. fuel_T is left
# out on purpose — no single T₀ on the 15–20 kK radiation-equilibrium profile.
_T_REF_K = {
    'beo_T': 1698.33,
    'h2_T':  6670.0,
}

def print_coefficient_summary(results):
    order = ['fuel_T', 'h2_rho', 'fuel_rho', 'beo_T', 'h2_T', 'fuel_radius']
    print('\n  variable       UARL S=(δk/k)/(Δx/x₀)      practical')
    print('  ----------     ----------------------     --------------------')
    for v in order:
        prac = practical_coefficient(results, v)
        if prac is None:
            continue
        val, err, unit = prac

        if v in ('h2_rho', 'fuel_rho', 'power', 'fuel_radius'):
            S = log_sensitivity(results, v)                       # delta already Δx/x₀
        elif v in _T_REF_K:
            S = log_sensitivity(results, v, ref_value=_T_REF_K[v])
        else:
            S = None                                             # fuel_T: no single T₀

        s_str = f'{S[0]:+.4f} ± {S[1]:.4f}' if S else '        —'
        print(f'  {v:<12s}   {s_str:>20s}      {val:+8.3f} ± {err:.3f} {unit}')

def plot_kinetic_parameters(results, panels, suptitle='GCR kinetic parameters'):
    """Plot β_eff and Λ_eff vs perturbation parameter for every sweep variable.

    Called automatically from compute_reactivity_and_plot() when kinetics
    data are present. Can also be called directly on a loaded results list.

    Parameters
    ----------
    results   : list[dict]  – same structure as returned by run_sensitivity_scan()
    panels    : list[str]   – ordered list of variable names (e.g. ['fuel_T', 'h2_rho'])
    suptitle  : str         – figure super-title (coupling-assumption note appended)
    """
    import matplotlib.pyplot as plt

    if not any('beta_eff' in r for r in results):
        print('No kinetic parameters in results (COLLECT_KINETIC_PARAMS was False).')
        return

    # Variable metadata: (subset_list, x_scale, xlabel, marker, color)
    _meta = {
        'fuel_T':   ([r for r in results if r['variable'] == 'fuel_T'],
                     1.0,   r'$\Delta T_{\mathrm{fuel}}$ (K)',      'o', 'C0'),
        'h2_rho':   ([r for r in results if r['variable'] == 'h2_rho'],
                     1.0,   r'$\delta\rho_{\mathrm{H}_2} / \rho_{\mathrm{H}_2,0}$', 's', 'C1'),
        'fuel_rho': ([r for r in results if r['variable'] == 'fuel_rho'],
                     100.0, r'$\Delta \rho_{\mathrm{fuel}}$ (%)',    'D', 'C3'),
        'beo_T':    ([r for r in results if r['variable'] == 'beo_T'],
                     1.0,   r'$\Delta T_{\mathrm{BeO}}$ (K)',        'v', 'C4'),
        'h2_T':     ([r for r in results if r['variable'] == 'h2_T'],
                     1.0,   r'$\Delta T_{\mathrm{H}_2}$ (K)',        'P', 'C5'),
        'fuel_radius': ([r for r in results if r['variable'] == 'fuel_radius'],
                        1.0,   r'$\delta R / R_0$  [mm/mm]',                   'h', 'C6'),
    }

    n_panels = len(panels)
    if n_panels == 0:
        return

    def _make_fig(n):
        if n <= 3:
            fig, axes = plt.subplots(2, n, figsize=(5.5 * n, 9), squeeze=False)
        elif n == 4:
            fig, axes = plt.subplots(4, 2, figsize=(11, 18), squeeze=False)
        else:
            fig, axes = plt.subplots(4, 3, figsize=(16.5, 18), squeeze=False)
            for ax in axes.flatten()[2 * n:]:
                ax.axis('off')
        return fig, axes

    fig, axes = _make_fig(n_panels)

    # Row 0: β_eff  |  Row 1: Λ_eff  (repeated for 4-panel+ layouts)
    if n_panels <= 3:
        beta_axes  = axes[0]   # shape (n_panels,)
        gen_axes   = axes[1]
    else:
        # Flatten and slice manually for generality
        flat = axes.flatten()
        beta_axes = flat[:n_panels]
        gen_axes  = flat[n_panels:2 * n_panels]

    baseline = next((r for r in results if r['variable'] == 'baseline'), None)
    beta_ref = baseline.get('beta_eff') if baseline else None
    gen_ref  = baseline.get('gen_time_s') if baseline else None

    for i, panel in enumerate(panels):
        sub, scale, xlabel, mk, col = _meta[panel]
        sub_sorted = sorted(sub, key=lambda r: r['delta'])

        x  = np.array([scale * r['delta'] for r in sub_sorted])
        b  = np.array([r.get('beta_eff', np.nan)         for r in sub_sorted])
        sb = np.array([r.get('sigma_beta_eff', 0.0)      for r in sub_sorted])
        g  = np.array([r.get('gen_time_s', np.nan)       for r in sub_sorted])
        sg = np.array([r.get('sigma_gen_time_s', 0.0)    for r in sub_sorted])

        # β_eff panel
        ax_b = beta_axes[i]
        ax_b.errorbar(x, b * 1e3, yerr=sb * 1e3,  # display in units of 1e-3
                      marker=mk, color=col, capsize=3, linestyle='none', label='data')
        if beta_ref is not None:
            ax_b.axhline(beta_ref * 1e3, color='grey', lw=0.8, linestyle=':',
                         label=rf'baseline $\beta = {beta_ref*1e3:.2f} \times 10^{{-3}}$')
        ax_b.set_xlabel(xlabel)
        ax_b.set_ylabel(r'$\beta_\mathrm{eff}$ ($\times 10^{-3}$)')
        ax_b.set_title(f'β_eff — {panel.replace("_", " ")}')
        ax_b.grid(alpha=0.3)
        ax_b.legend(fontsize=8)

        # Λ_eff panel
        ax_g = gen_axes[i]
        ax_g.errorbar(x, g * 1e6, yerr=sg * 1e6,  # display in µs
                      marker=mk, color=col, capsize=3, linestyle='none', label='data')
        if gen_ref is not None:
            ax_g.axhline(gen_ref * 1e6, color='grey', lw=0.8, linestyle=':',
                         label=rf'baseline $\Lambda = {gen_ref*1e6:.2f}$ µs')
        ax_g.set_xlabel(xlabel)
        ax_g.set_ylabel(r'$\Lambda_\mathrm{eff}$ (µs)')
        ax_g.set_title(rf'$\Lambda_\mathrm{{eff}}$ — {panel.replace("_", " ")}')
        ax_g.grid(alpha=0.3)
        ax_g.legend(fontsize=8)

    fig.suptitle(suptitle.replace('reactivity feedback', 'kinetic parameters'))
    fig.tight_layout()

    plot_suffix = '_' + '_'.join(panels) if len(panels) < 6 else ''
    pdf3 = os.path.join(RESULTS_DIR, f'kinetics{plot_suffix}.pdf')
    fig.savefig(pdf3, dpi=150)
    print(f'Kinetic parameters plot written to {pdf3}')
    return fig


def load_baseline(results_path=None):
    """Load a pre-computed baseline result from a JSON file.

    Accepts either a standalone baseline.json (a single dict) or any
    keff_results*.json list — the first entry whose 'variable' is 'baseline'
    is returned.

    Parameters
    ----------
    results_path : str, optional
        Path to a JSON file.  Defaults to <RESULTS_DIR>/baseline.json.

    Returns
    -------
    dict
        The baseline result dict, ready to pass as ``baseline_result`` to
        :func:`run_sensitivity_scan`.

    Raises
    ------
    FileNotFoundError
        If the file does not exist.
    ValueError
        If no baseline entry is found in the file.
    """
    if results_path is None:
        results_path = os.path.join(RESULTS_DIR, 'baseline.json')

    if not os.path.exists(results_path):
        raise FileNotFoundError(
            f'No baseline file at {results_path!r}. '
            f'Run a sweep without --baseline-json first so the file is created, '
            f'or point --baseline-json at an existing keff_results*.json.'
        )

    with open(results_path, 'r') as f:
        data = json.load(f)

    # Support both a bare dict (standalone baseline.json) and a list
    # (keff_results*.json with multiple entries).
    if isinstance(data, dict):
        if data.get('variable') != 'baseline':
            raise ValueError(
                f'{results_path!r} is a dict but its "variable" field is '
                f'{data.get("variable")!r}, not "baseline".'
            )
        return data

    # It's a list — find the first baseline entry.
    for entry in data:
        if entry.get('variable') == 'baseline':
            print(f'Loaded baseline from {results_path!r}  '
                  f'(k_eff = {entry["keff"]:.5f} +/- {entry["sigma"]:.5f})')
            return entry

    print(f'Loaded {len(results)} cases from {results_path}')
    return compute_reactivity_and_plot(results)

def extend_h2_T_scan(extra_deltas, results_path=None):
    """Run additional h2_T cases and merge them into an existing results file.

    Skips any delta already present and skips the baseline rerun -- much
    cheaper than re-running the whole sweep.

    Parameters
    ----------
    extra_deltas : iterable of float
        Additional ΔT values in kelvin to evaluate (e.g. [-2000, +2000]).
    results_path : str, optional
        JSON file to extend. Defaults to <RESULTS_DIR>/keff_results_h2_T.json.

    Returns
    -------
    list[dict]
        The merged results list (baseline + all h2_T points), suitable
        for handing straight to compute_reactivity_and_plot().
    """
    if results_path is None:
        results_path = os.path.join(RESULTS_DIR, 'keff_results_h2_T.json')

    if not os.path.exists(results_path):
        raise FileNotFoundError(
            f'No cached results at {results_path!r}. '
            f'Run the h2_T sweep first.'
        )

    with open(results_path, 'r') as f:
        results = json.load(f)

    # Sanity check: coupling flag must match the original scan, otherwise
    # the new points are not on the same curve as the existing ones.
    existing_h2T = [r for r in results if r['variable'] == 'h2_T']

    existing_deltas = {round(r['delta'], 6) for r in existing_h2T}

    base_config = GCRConfig(**BASE_CONFIG_KWARGS)

    for dT in extra_deltas:
        if round(float(dT), 6) in existing_deltas:
            print(f'h2_T ΔT = {dT:+g} K already in results, skipping')
            continue
        results.append(run_case('h2_T', float(dT), base_config))

    with open(results_path, 'w') as f:
        json.dump(results, f, indent=2)
    print(f'Extended results written to {results_path}')

    return results

