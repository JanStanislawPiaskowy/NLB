"""
sensitivity_analysis.py
=======================

Reactivity-feedback sensitivity scan on the GCR model defined in GCR_test.py.

For each of five independent variables the script records k_eff at baseline
and at a series of perturbed states:

    1. fuel temperature: ΔT in {-50, -25, -10, -5, +5, +10, +25, +50} K,
       applied additively to every fuel material (fuel_inner / fuel_outer,
       all axial layers). Per-layer baseline temperatures are unchanged
       relative to each other; only the common offset changes.

    2. hydrogen propellant density: fractional deltas
       {-3, -2, -1, +1, +2, +3} %, applied multiplicatively to every H2
       propellant material (main channel + layers + header + liner + tori).

    3. cavity power: fractional deltas {-10, -5, +5, +10} %, applied by
       swapping in a propellant profile pre-computed at the perturbed power
       AND scaling GCRConfig.Q_total by the same factor. The perturbed
       profile changes the per-layer H2 temperature and density; the scaled
       Q_total feeds into GCR.fuel_temperature_from_h2() so the radiation-
       equilibrium fuel temperature also shifts. Profiles are expected at
           settings/h2_density_profile_power_{sign}{pct:02d}pct.npz
       and are generated by hydrogen_propellant_profile.py.

    4. fuel density: fractional deltas {-10, -5, +5, +10} %, applied
       multiplicatively to every fuel material (fuel_inner / fuel_outer,
       all axial layers) WITHOUT touching their temperature. This isolates
       the pure number-density component of the fuel feedback, with no
       cross-section interpolation effects in play.

    5. BeO reflector temperature: ΔT in {-200, -100, -50, +50, +100, +200} K,
       applied additively to the single 'BeO' material that fills the
       inter-cavity moderator slabs and the top cap. The bulk density is
       held constant -- moderator thermal expansion is small compared to
       the S(alpha, beta) temperature effect that dominates the reactivity
       response.

    6. H2 propellant temperature: ΔT in {-200, -100, -50, +50, +100, +200} K,
       applied additively to every H2 propellant material (main channel +
       layers + header + liner + tori). Per-layer baseline temperatures are
       unchanged relative to each other; only the common offset changes.
       Like the fuel, hydrogen is a gas, so the realistic coupled feedback
       follows rho * T = const at constant pressure -- enable this with
       COUPLE_DENSITY_TO_H2_T. Set it to False to isolate the
       cross-section / thermal-scattering-kernel temperature effect.

Reactivity change:
                 rho = (k - k_ref) / (k * k_ref)        [pcm when * 1e5]

From the sweep the script fits a weighted least-squares line through the
(delta, rho) points and reports:

    alpha_T     in pcm per kelvin                (fuel temperature)
    alpha_rho   in pcm per % H2 density change
    alpha_Q     in pcm per % cavity-power change
    alpha_rho_F in pcm per % fuel density change
    alpha_BeO   in pcm per kelvin                (BeO reflector temperature)
    alpha_T_H2  in pcm per kelvin                (H2 propellant temperature)

with slope uncertainties from the weighted-fit covariance.

Placement: drop this file next to GCR.py and run
           `python sensitivity_analysis.py`.
Runtime:   1 baseline + 8 fuel-T + 6 H2-rho = 15 OpenMC runs. Budget several
           hours at 50 batches × 15,000 particles.

Fuel-temperature coupling assumption
------------------------------------
The fuel is a gas. At constant pressure an ideal gas satisfies rho * T = const,
so heating the fuel also thins it. The realistic GCR temperature feedback is
therefore the *combined* Doppler-plus-density effect -- this is the default.

Set COUPLE_DENSITY_TO_FUEL_T = False to hold fuel density fixed and measure
a pure Doppler coefficient instead.

Cross-section note
------------------
OpenMC interpolates cross sections in sqrt(T) between the processed library
temperatures. For small ΔT at 15-20 kK, the response you measure is the
slope of that interpolation, not an independent Doppler measurement. The
fit is still meaningful as "the coefficient embedded in the library as
used", but interpret it accordingly.
"""

