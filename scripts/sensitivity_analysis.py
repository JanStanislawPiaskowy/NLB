def _is_fuel(mat):
    # set_materials() names the single-layer fuel 'fuel' / 'fuel_outer'.
    # _create_layered_fuel_materials() names layered ones
    # 'fuel_inner_layer_k' / 'fuel_outer_layer_k'.
    name = mat.name or ''
    return (name == 'fuel'
            or name.startswith('fuel_inner')
            or name.startswith('fuel_outer'))


def _is_propellant_h2(mat):
    name = mat.name or ''
    return (name.startswith('hydrogen_layer')
        )


def _is_beo_reflector(mat):
    # set_materials() creates a single material called 'BeO' that fills the
    # inter-cavity moderator slabs and the top cap.
    name = mat.name or ''
    return name == 'BeO'


def _unique_materials(core):
    """Yield each material object once, de-duplicating by id().

    _create_layered_fuel_materials() aliases 'fuel_inner' / 'fuel_outer' in
    core.materials to the layer-0 objects, so the same object appears under
    two dict keys. Without the de-dup a naive loop perturbs layer 0 twice.
    """
    seen = set()
    for mat in core.materials.values():
        if id(mat) in seen:
            continue
        seen.add(id(mat))
        yield mat


# Perturbation

def perturb_fuel_temperature(core, dT_kelvin):
    """Shift every fuel material's temperature by dT_kelvin (additive).
    """
    for mat in _unique_materials(core):
        if not _is_fuel(mat):
            continue

        T_old = mat.temperature
        T_new = T_old + dT_kelvin
        mat.temperature = T_new


def perturb_h2_density(core, factor):
    """Scale density of every propellant-H2 material by `factor`."""
    for mat in _unique_materials(core):
        if not _is_propellant_h2(mat):
            continue
        if mat.density_units != 'g/cm3':
            raise RuntimeError(
                f'H2 material {mat.name!r} has density_units='
                f'{mat.density_units!r}; expected g/cm3.'
            )
        mat.set_density('g/cm3', mat.density * factor)


def perturb_fuel_density(core, factor):
    """Scale density of every fuel material by `factor`, leaving temperature alone."""
    for mat in _unique_materials(core):
        if not _is_fuel(mat):
            continue
        if mat.density_units != 'g/cm3':
            raise RuntimeError(
                f'Fuel material {mat.name!r} has density_units='
                f'{mat.density_units!r}; expected g/cm3.'
            )
        mat.set_density('g/cm3', mat.density * factor)


def perturb_beo_temperature(core, dT_kelvin):
    """Shift the BeO reflector/moderator material's temperature by dT_kelvin."""
    for mat in _unique_materials(core):
        if not _is_beo_reflector(mat):
            continue
        T_old = mat.temperature
        T_new = T_old + dT_kelvin
        mat.temperature = T_new


def perturb_h2_temperature(core, dT_kelvin):
    """Shift every propellant-H2 material's temperature by dT_kelvin.

    Applies to the main channel material, all axial-layer slices, the header,
    the liner, and the tori -- per-layer baseline differences along the
    channel are preserved.
    """
    for mat in _unique_materials(core):
        if not _is_propellant_h2(mat):
            continue

        T_old = mat.temperature
        T_new = T_old + dT_kelvin
        mat.temperature = T_new


def print_material_temperatures(core, label=''):
    """Print temperature (and density) of every unique material.

    Called inside run_case() right after the perturbation has been applied,
    so the printed values are exactly what OpenMC will see for this case.
    Materials are grouped by the same classifiers used for perturbations
    (fuel / H2 propellant / BeO reflector / other), so it is easy to scan
    the relevant block and verify a sweep landed where expected.

    Density is shown alongside temperature because several perturbations
    couple the two (h2_rho, fuel_rho, optionally fuel_T and h2_T via the
    COUPLE_DENSITY_TO_*_T flags).
    """
    header = f'  Material state{(": " + label) if label else ""}:'
    print(header)

    fuel_mats, h2_mats, beo_mats, other_mats = [], [], [], []
    for mat in _unique_materials(core):
        if _is_fuel(mat):
            fuel_mats.append(mat)
        elif _is_propellant_h2(mat):
            h2_mats.append(mat)
        elif _is_beo_reflector(mat):
            beo_mats.append(mat)
        else:
            other_mats.append(mat)

    def _print_group(title, mats, show_rho=True):
        if not mats:
            return
        print(f'    {title} ({len(mats)}):')
        # Stable ordering: by name, so layer indices come out in numeric order
        # for fuel_inner_layer_0, fuel_inner_layer_1, ...
        for mat in sorted(mats, key=lambda m: m.name or ''):
            T = mat.temperature
            line = f'      {(mat.name or "<unnamed>"):<32s}  T = {T:8.2f} K'
            if show_rho:
                line += f'   rho = {mat.density:.4e} g/cm3'
            print(line)

    _print_group('Fuel',          fuel_mats)
    _print_group('H2 propellant', h2_mats)
    _print_group('BeO reflector', beo_mats)
    # 'Other' materials (graphite, BeO supports, void, etc.) are never
    # perturbed by this script -- show them without density to keep the
    # block compact.
    _print_group('Other',         other_mats, show_rho=False)


