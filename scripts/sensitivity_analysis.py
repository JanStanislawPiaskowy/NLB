def build_core(config, run_subdir):
    core = GCR(config)
    core.output_dir = os.path.join(RUNS_DIR, run_subdir)
    os.makedirs(core.output_dir, exist_ok=True)
    core.set_materials()
    if config.n_axial_layers > 1:
        core._create_layered_propellant_materials()
        core._create_layered_fuel_materials()

    # Scale fuel to the critical state BEFORE any perturbation is applied.
    # Matches main() in GCR_test.py.
    FUEL_DENSITY_ALPHA = 2.0240
    _seen = set()
    for mat in core.materials.values():
        if id(mat) in _seen:
            continue
        _seen.add(id(mat))
        name = mat.name or ''
        if (name == 'fuel' or name.startswith('fuel_inner')
                or name.startswith('fuel_outer')):
            mat.set_density('g/cm3', mat.density * FUEL_DENSITY_ALPHA)

    return core


def build_geometry_and_export(core):
    cfg = core.config

    core.build_cavity(tilt=cfg.tilt)

    sixty_deg = np.pi / 3
    hex_side_to_radius = 1.4
    hl  = cfg.r_inlet * hex_side_to_radius
    phi = 2 * cfg.tilt

    for i in range(6):
        y0 = hl * np.sin(sixty_deg) * (1 + np.cos(phi))
        z0 = hl * np.sin(sixty_deg) * np.sin(phi)
        theta = -i * np.pi / 3
        xp = -y0 * np.sin(theta)
        yp =  y0 * np.cos(theta)
        zp = z0
        core.build_cavity(
            x0=xp, y0=yp, z0=zp,
            tilt=cfg.tilt,
            cavity_angle_zz=theta,
            cavity_angle_xx=phi,
        )

    z_off = np.sin(sixty_deg) * hl / np.tan(phi)
    core.create_bounding_sphere(offset=z_off)
    core.build_moderator()
    core.build_end_moderator()
    core.build_nozzle_end()
    core.resolve_cavity_overlaps()
    core.set_source(batches=N_BATCHES, inactive=N_INACTIVE, n=N_PARTICLES)

    core.settings.temperature = {
        'method': 'interpolation',
        'tolerance': 300.0,
        'multipole': False,
    }
    core.settings.export_to_xml(os.path.join(core.output_dir, 'settings.xml'))

    core.export_geometry()

    # IFP kinetic-parameter tallies (β_eff, Λ_eff). Must be called AFTER
    # export_geometry() so the output directory already exists and after
    # set_source() so ifp_n_generation is already set in settings.
    if COLLECT_KINETIC_PARAMS:
        core.add_kinetics_tally(num_groups=6)


def _extract_keff_h5py(sp_path):
    """Fallback k_eff extractor that reads directly from the statepoint HDF5.

    Used when openmc.StatePoint cannot be opened because summary.h5 is absent
    or corrupt.  Kinetics parameters are not available via this path (they
    require tally linking which needs the summary), so an empty dict is
    returned for them.
    """
    import h5py
    with h5py.File(sp_path, 'r') as f:
        # 'k_combined' stores [mean, std_dev] of the combined k-eff estimator.
        k = f['k_combined'][()]
        nominal = float(k[0])
        stddev  = float(k[1])
    print('    [warn] k_eff extracted via h5py fallback '
          '(summary.h5 unavailable — kinetics parameters skipped).')
    return nominal, stddev, {}


def run_and_extract_keff(core):
    # Disable HDF5 POSIX file locking so summary.h5 can be written on NFS /
    # shared filesystems (prevents errno=11 "Resource temporarily unavailable").
    os.environ['HDF5_USE_FILE_LOCKING'] = 'FALSE'

    sp_path = os.path.join(core.output_dir, f'statepoint.{core.settings.batches}.h5')
    summary_path = os.path.join(core.output_dir, 'summary.h5')

    try:
        core.run(dry_run=False, map_geometry=False)
    except RuntimeError as e:
        # OpenMC can exit non-zero during teardown *after* the statepoint
        # is fully written. Treat that as success iff the file is readable.
        if not os.path.exists(sp_path):
            raise
        print(f'    [warn] OpenMC aborted on exit but statepoint exists — continuing. ({e})')

    # If summary.h5 exists but is corrupt (partial write from a prior locking
    # failure), remove it so StatePoint does not crash with
    # "bad object header version number".
    if os.path.exists(summary_path):
        try:
            import h5py as _h5
            _h5.File(summary_path, 'r').close()
        except OSError:
            print('    [warn] summary.h5 is corrupt — removing it before StatePoint open.')
            os.remove(summary_path)


    sp = openmc.StatePoint(sp_path)

    keff    = sp.keff
    nominal = float(keff.nominal_value)
    stddev  = float(keff.std_dev)

    kinetics = {}
    if COLLECT_KINETIC_PARAMS:
        kin = sp.get_kinetics_parameters()
        beta = kin.beta_effective      #total β_eff
        gen  = kin.generation_time     # Λ_eff in seconds
        kinetics['beta_eff']         = float(beta.nominal_value)
        kinetics['sigma_beta_eff']   = float(beta.std_dev)
        kinetics['gen_time_s']       = float(gen.nominal_value)
        kinetics['sigma_gen_time_s'] = float(gen.std_dev)


    sp.close()
    return nominal, stddev, kinetics


## Running the cases

def _tag(variable, delta):
    if variable == 'baseline':
        return 'baseline'
    if variable == 'fuel_T':
        sign = 'p' if delta >= 0 else 'm'
        return f'fuel_T_{sign}{abs(int(delta)):02d}K'
    if variable == 'h2_rho':
        sign = 'p' if delta >= 0 else 'm'
        return f'h2_rho_{sign}{abs(delta) * 100:02.0f}pct'
    if variable == 'fuel_rho':
        sign = 'p' if delta >= 0 else 'm'
        return f'fuel_rho_{sign}{abs(delta) * 100:02.0f}pct'
    if variable == 'beo_T':
        sign = 'p' if delta >= 0 else 'm'
        return f'beo_T_{sign}{abs(int(delta)):02d}K'
    if variable == 'h2_T':
        sign = 'p' if delta >= 0 else 'm'
        return f'h2_T_{sign}{abs(int(delta)):02d}K'
    if variable == 'fuel_radius':
        sign = 'p' if delta >= 0 else 'm'
        return f'fuel_radius_{sign}{abs(delta) * 100:02.0f}pct'
    raise ValueError(variable)


def run_case(variable, delta, base_config):
    """delta is in K for variable='fuel_T', dimensionless fraction for
    'h2_rho', 'fuel_rho', and 'power'."""
    tag = _tag(variable, delta)

    print(f'\n=== Case: {tag} ===')


    if variable == 'fuel_radius':
        # Scale R0, R1, R2 together so the internal cloud structure is preserved.
        # R3 (transparent wall) is left unchanged.
        R2_base = base_config.R2
        config_for_case = replace(
            base_config,
            R0=base_config.R0 * (1.0 + delta),
            R1=base_config.R1 * (1.0 + delta),
            R2=R2_base * (1.0 + delta),
        )
        R2_new = config_for_case.R2
        R3 = base_config.R3
        print(f'    R2: {R2_base:.4f} -> {R2_new:.4f} cm  '
              f'(gap to R3={R3:.2f} cm: {R3 - R2_new:.4f} cm)')
        if R2_new >= R3:
            raise ValueError(
                f'Perturbed R2 ({R2_new:.4f} cm) exceeds or meets the '
                f'transparent wall R3 ({R3:.4f} cm). Reduce the delta.'
            )
    elif variable not in ('fuel_radius',):
        config_for_case = base_config

    core = build_core(config_for_case, run_subdir=tag)

    if variable == 'fuel_T' and delta != 0.0:
        perturb_fuel_temperature(core, delta)          # kelvin, additive
    elif variable == 'h2_rho' and delta != 0.0:
        perturb_h2_density(core, 1.0 + delta)          # fractional, multiplicative
    elif variable == 'fuel_rho' and delta != 0.0:
        perturb_fuel_density(core, 1.0 + delta)        # fractional, multiplicative
    elif variable == 'beo_T' and delta != 0.0:
        perturb_beo_temperature(core, delta)           # kelvin, additive
    elif variable == 'h2_T' and delta != 0.0:
        perturb_h2_temperature(core, delta)            # kelvin, additive
    elif variable == 'fuel_radius' and delta != 0.0:
        # Volume scales as (1+delta)^2 → rescale density
        # to conserve total fuel inventory (constant number of U atoms).
        perturb_fuel_density(core, 1.0 / (1.0 + delta)**2)
        print_u233_mass_estimate(core.materials, config_for_case)

    # Validation: print the post-perturbation state of every material so
    # the user can confirm the sweep landed on the right materials with
    # the right shift before OpenMC spends an hour proving it ran.
    print_material_temperatures(core, label=tag)
    core.apply_beo_sab()
    build_geometry_and_export(core)
    keff, sigma, kinetics = run_and_extract_keff(core)

    print(f'    --> k_eff = {keff:.5f} +/- {sigma:.5f}')
    if kinetics:
        beta = kinetics.get('beta_eff')
        s_beta = kinetics.get('sigma_beta_eff', 0.0)
        gen  = kinetics.get('gen_time_s')
        s_gen = kinetics.get('sigma_gen_time_s', 0.0)
        print(f'    --> β_eff = {beta:.5f} +/- {s_beta:.5f}')
        print(f'    --> Λ_eff = {gen:.4e} +/- {s_gen:.4e} s')
        # reactivity in dollars (useful sanity check)
        if beta and beta > 0:
            rho_abs_pcm = 1e5 * (keff - 1.0) / keff
            print(f'    --> ρ / β_eff = {rho_abs_pcm / (beta * 1e5):+.4f} $')

    result = {
        'variable': variable,
        'delta':    float(delta),
        'keff':     float(keff),
        'sigma':    float(sigma),
        'tag':      tag
    }
    result.update(kinetics)   # adds beta_eff / sigma_beta_eff / gen_time_s / sigma_gen_time_s if collected
    return result


